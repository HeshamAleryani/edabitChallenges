// 1-Convert Minutes into Seconds:
function convert(minutes) {
  return minutes * 60;
}

// -----------
// 2-Return the Remainder from Two Numbers:
function remainder(x, y) {
  return x % y;
}

// -----------
// 3-Convert Age to Days

function calcAge(age) {
  return age * 365;
}

// -----------
//4-Return the Next Number from the Integer Passed
function addition(num) {
  return ++num;
}
// -----------
//4-Area of a Triangle
function triArea(base, height) {
  return (base * height) / 2;
}
// -----------
//5-Return the First Element in an Array

function getFirstValue(arr) {
  return arr.shift();
}
// -----------
//6-Correct the Mistakes

function squared(b) {
  return Math.pow(b, 2);
}

// -----------
//7-Less Than 100?
function lessThan100(a, b) {
  return a + b < 100 ? true : false;
}
// -----------
//8-Power Calculator
function circuitPower(voltage, current) {
  return voltage * current;
}
// -----------
//9-Sum of Polygon Angles
function sumPolygon(n) {
  return (n - 2) * 180;
}
// -----------
//10-Find the Perimeter of a Rectangle
function findPerimeter(length, width) {
  return 2 * (length + width);
}
